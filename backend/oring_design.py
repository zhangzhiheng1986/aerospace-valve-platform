    delta_T = op_temp - 20.0
    thermal_f = 1.0 + mat["thermal_exp"] * delta_T
    swell_f = 1.0 + mat["fluid_swell"] / 100.0
    area_expanded = area_oring * thermal_f * swell_f
    area_required = area_expanded * (1.0 + vol_reserve / 100.0)
    gw_nom = area_required / groove_depth
    gw_nom = max(gw_nom, cs * 1.2)  # minimum installability
    gw_min = round(gw_nom - 0.13, 3)
    gw_max = round(gw_nom + 0.13, 3)
    gw_nom = round(gw_nom, 3)

    # 4. Groove diameters (per SAE AS4716C)
    od_mm = oring["od_mm"]  # = id_mm + 2*cs
    if pressure_dir == "INTERNAL":
        g_id = round(id_mm, 3)   # seals on ID surface
        g_od = round(id_mm + 2 * gw_nom, 3)
    elif pressure_dir == "EXTERNAL":
        g_id = round(od_mm - 2 * gw_nom, 3)  # seals on OD surface
        g_od = round(od_mm, 3)
    else:  # BIDIRECTIONAL
        g_id = round(id_mm - gw_nom, 3)
        g_od = round(od_mm + gw_nom, 3)

    # 5. Corner radii
    if cs <= 1.80:    r1, r2 = 0.4, 0.2
    elif cs <= 2.65:  r1, r2 = 0.4, 0.2
    elif cs <= 3.55:  r1, r2 = 0.6, 0.2
    elif cs <= 5.33:  r1, r2 = 0.6, 0.2
    else:             r1, r2 = 1.0, 0.2

    # 6. Performance evaluation
    # Actual squeeze
    actual_squeeze = round((cs - groove_depth) / cs * 100.0, 2) if cs > 0 else 0

    # Stretch ratio (groove_id vs o-ring id) 
    if g_id >= id_mm:
        stretch = round((g_id - id_mm) / id_mm * 100.0, 2) if id_mm > 0 else 0
    else:
        stretch = round((id_mm - g_id) / g_id * 100.0, 2) if g_id > 0 else 0

    # Gland fill
    mean_dia = id_mm + cs
    vol_oring = math.pi * mean_dia * area_oring
    vol_oring_exp = vol_oring * thermal_f * swell_f
    groove_mean_dia = (g_id + g_od) / 2.0
    groove_cs_area = gw_nom * groove_depth
    vol_groove = math.pi * groove_mean_dia * groove_cs_area
    gland_fill = round(vol_oring_exp / vol_groove * 100.0, 2) if vol_groove > 0 else 100

    # Contact stress
    epsilon = actual_squeeze / 100.0
    sigma_mean = E * epsilon * (1.0 + epsilon) + sys_pressure * 0.3
    sigma_peak = sigma_mean * 1.5
    R = cs / 2.0
    F_per_l = sigma_mean * (cs * epsilon)
    E_star = E / (1.0 - 0.25)  # poisson ~0.5 for rubber
    contact_w = math.sqrt(4.0 * F_per_l * R / (math.pi * E_star)) if E_star > 0 else cs * epsilon

    # Extrusion gap
    if cs <= 1.78:    base_gap = 0.10
    elif cs <= 2.62:  base_gap = 0.13
    elif cs <= 3.53:  base_gap = 0.15
    elif cs <= 5.33:  base_gap = 0.20
    else:             base_gap = 0.25
    hf = (mat["hardness"] / 70.0) ** 1.5
    if sys_pressure <= 5:    pf = 1.0
    elif sys_pressure <= 10: pf = 0.8
    elif sys_pressure <= 15: pf = 0.6
    elif sys_pressure <= 21: pf = 0.4
    else:                    pf = 0.2
    ext_gap_allow = round(base_gap * hf * pf, 4)
    need_backup = (sys_pressure >= 9.8) or (ext_gap_allow < 0.05)

    # Leakage rate (Roth model)
    h_eff = roughness * 1e-6 * math.exp(-sigma_mean / (E * 0.1))
    h_eff = max(h_eff, 1e-9)
    L_leak = max(contact_w * 1e-3, 1e-4)
    delta_P = sys_pressure * 1e6
    K_seal = max(1.0 - actual_squeeze / 100.0, 0.01)
    circumference = math.pi * mean_dia * 1e-3
    Q = circumference * h_eff**3 * delta_P / (12.0 * 1e-3 * L_leak) * K_seal * 1.5

    # Friction force (dynamic only)
    friction = 0.0
    if application in ("DYNAMIC_RECIPROCATING", "DYNAMIC_ROTARY"):
        f_c = min(0.3 + (mat["hardness"] - 60) * 0.005 + actual_squeeze * 0.005, 0.6)
        F_start = f_c * math.pi * mean_dia * contact_w * E * actual_squeeze / 100.0
        F_pressure = 0.05 * math.pi * mean_dia * contact_w * sys_pressure
        friction = round(F_start + F_pressure, 2)

    # Seal factor
    seal_factor = round(sigma_peak / sys_pressure, 3) if sys_pressure > 0 else 9999

    # Leakage level
    if Q < 1e-10:   leak_level = "A - / (aerospace)"
    elif Q < 1e-8:  leak_level = "B - / (aviation hydraulic)"
    elif Q < 1e-6:  leak_level = "C - / (industrial)"
    elif Q < 1e-4:  leak_level = "D - / (optimize)"
    else:            leak_level = "E - / (fail)"

    # Safety margin assessment
    safety_margin = 1.0
    if actual_squeeze < sq_min:
        warnings_list.append(f"Squeeze {actual_squeeze:.1f}% < min {sq_min:.0f}%")
        safety_margin *= 0.7
    elif actual_squeeze > sq_max:
        warnings_list.append(f"Squeeze {actual_squeeze:.1f}% > max {sq_max:.0f}%")
        safety_margin *= 0.8
    if gland_fill > 95:
        warnings_list.append(f"Fill {gland_fill:.1f}% too high, extrusion risk")
        safety_margin *= 0.6
    elif gland_fill > 90:
        warnings_list.append(f"Fill {gland_fill:.1f}% high, increase groove width")
        safety_margin *= 0.8
    elif gland_fill < 50:
        warnings_list.append(f"Fill {gland_fill:.1f}% too low")
        safety_margin *= 0.7
    if seal_factor < 1.2:
        warnings_list.append(f"Seal factor {seal_factor:.2f} < 1.2")
        safety_margin *= 0.5
    elif seal_factor < 1.5:
        warnings_list.append(f"Seal factor {seal_factor:.2f} < 1.5")
        safety_margin *= 0.8
    if op_temp > mat["max_temp"]:
        warnings_list.append(f"Temp {op_temp}C > max {mat['max_temp']}C")
        safety_margin *= 0.3
    elif op_temp < mat["min_temp"]:
        warnings_list.append(f"Temp {op_temp}C < min {mat['min_temp']}C")
        safety_margin *= 0.3
    elif op_temp > mat["max_temp"] * 0.85:
        warnings_list.append(f"Temp {op_temp}C near limit {mat['max_temp']}C")
        safety_margin *= 0.7

    # Recommendations
    if need_backup:
        recommendations.append(f"Pressure {sys_pressure:.1f}MPa, add back-up ring")
        if pressure_dir == "BIDIRECTIONAL":
            recommendations.append("Bidirectional pressure: add 2 back-up rings")
    if gland_fill > 85:
        recommendations.append(f"Increase vol reserve from {vol_reserve:.0f}% to {vol_reserve+15:.0f}%")
    if roughness > 0.8:
        recommendations.append("Surface Ra should be <= 0.8 um")

    # Conclusion
    critical = [w for w in warnings_list if any(k in w for k in ["< min", "> max", "too high", "< 1.2", "> max", "< min"])]
    if not warnings_list:
        conclusion = "PASS - All checks OK"
    elif not critical:
        conclusion = f"PASS (with {len(warnings_list)} notes)"
    else:
        conclusion = f"FAIL - {len(critical)} critical issue(s)"

    return {
        "o_ring": {
            "dash": oring["dash"],
            "id_mm": id_mm, "cs_mm": cs, "od_mm": od_mm,
            "id_nom_inch": oring["id_nom_inch"],
            "cs_nom_inch": oring["cs_nom_inch"],
            "id_tol_p": oring["id_tol_p"], "id_tol_m": oring["id_tol_m"],
            "cs_tol_p": oring["cs_tol_p"], "cs_tol_m": oring["cs_tol_m"],
            "cs_area_mm2": round(area_oring, 2),
        },
        "material": {
            "key": mat["name"].split()[0] + "_" + str(mat["hardness"]),
            "name": mat["name"], "code": mat["code"],
            "hardness": mat["hardness"], "E_MPa": E,
            "min_temp": mat["min_temp"], "max_temp": mat["max_temp"],
            "thermal_exp": mat["thermal_exp"], "fluid_swell": mat["fluid_swell"],
        },
        "groove": {
            "depth_nom": groove_depth,
            "depth_min": round(groove_depth - 0.05, 3),
            "depth_max": round(groove_depth + 0.10, 3),
            "width_nom": gw_nom, "width_min": gw_min, "width_max": gw_max,
            "groove_id": g_id, "groove_od": g_od,
            "r1": r1, "r2": r2, "Ra": roughness,
        },
        "performance": {
            "squeeze_pct": actual_squeeze,
            "squeeze_range": [sq_min, sq_target, sq_max],
            "stretch_pct": stretch,
            "gland_fill_pct": gland_fill,
            "contact_stress_mean_MPa": round(sigma_mean, 3),
            "contact_stress_peak_MPa": round(sigma_peak, 3),
            "contact_width_mm": round(contact_w, 3),
            "seal_factor": seal_factor,
            "leakage_rate_Pa_m3s": Q,
            "leak_level": leak_level,
            "friction_N": friction,
            "ext_gap_allowable_mm": ext_gap_allow,
            "need_backup_ring": need_backup,
        },
        "safety_margin": round(safety_margin, 3),
        "warnings": warnings_list,
        "recommendations": recommendations,
        "conclusion": conclusion,
    }


