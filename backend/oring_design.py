def _clean(v):
    """Replace non-JSON-safe float values"""
    if isinstance(v, float):
        if math.isinf(v): return 1e9 if v > 0 else 0
        if math.isnan(v): return 0
        return v
    if isinstance(v, dict):
        return {k: _clean(val) for k, val in v.items()}
    if isinstance(v, list):
        return [_clean(x) for x in v]
    return v


def _estimate_E(hardness):
    """Estimate Young's modulus from Shore A hardness (MPa)"""
    H = min(hardness, 95)
    return round((15.75 + 2.15 * H) / (100 - H), 2)


def _get_squeeze_target(cs_mm, application):
    """Get target squeeze ratio range for given CS and application type.
    Returns (min%, target%, max%)"""
    if application in ("STATIC_FACE", "STATIC_RADIAL"):
        if cs_mm <= 1.80:   return (20, 26, 30)
        elif cs_mm <= 2.65: return (18, 24, 28)
        elif cs_mm <= 3.55: return (16, 21, 25)
        elif cs_mm <= 5.33: return (14, 18, 22)
        else:               return (12, 15, 18)
    elif application == "DYNAMIC_RECIPROCATING":
        return (10, 12, 15)
    else:
        return (3, 5, 8)


def _search_oring(bore_or_rod_mm, is_bore_seal, cs_preferred_mm=None):
    """Search AS568 database for matching O-ring sizes"""
    results = []
    for dash, data in AS568_DATA.items():
        id_mm = data[1]
        cs_mm = data[3]
        if is_bore_seal:
            # Piston seal: O-ring ID slightly smaller than bore
            if bore_or_rod_mm * 0.85 <= id_mm <= bore_or_rod_mm * 0.98:
                if cs_preferred_mm is None or abs(cs_mm - cs_preferred_mm) < 0.5:
                    results.append({
                        "dash": dash,
                        "id_mm": id_mm, "cs_mm": cs_mm,
                        "od_mm": round(id_mm + 2 * cs_mm, 2),
                        "id_nom_inch": data[0], "cs_nom_inch": data[2],
                        "id_tol_p": data[4], "id_tol_m": data[5],
                        "cs_tol_p": data[6], "cs_tol_m": data[7],
                    })
        else:
            # Rod seal: O-ring ID slightly larger than rod
            if bore_or_rod_mm <= id_mm <= bore_or_rod_mm * 1.05:
                if cs_preferred_mm is None or abs(cs_mm - cs_preferred_mm) < 0.5:
                    results.append({
                        "dash": dash,
                        "id_mm": id_mm, "cs_mm": cs_mm,
                        "od_mm": round(id_mm + 2 * cs_mm, 2),
                        "id_nom_inch": data[0], "cs_nom_inch": data[2],
                        "id_tol_p": data[4], "id_tol_m": data[5],
                        "cs_tol_p": data[6], "cs_tol_m": data[7],
                    })
    results.sort(key=lambda x: abs(x["cs_mm"] - (cs_preferred_mm or 3.0)))
    return results


def design_oring(data: Dict) -> Dict:
    """Main API entry point for O-ring seal design"""
    bore_or_rod = float(data.get("bore_or_rod_dia_mm", 63.0))
    is_bore = bool(data.get("is_bore_seal", True))
    mat_key = data.get("material", "FKM_75")
    application = data.get("application", "STATIC_RADIAL")
    pressure_dir = data.get("pressure_dir", "INTERNAL")
    sys_pressure = float(data.get("system_pressure", 21.0))
    op_temp = float(data.get("operating_temp", 150))
    cs_preferred = data.get("cs_preferred_mm")
    if cs_preferred is not None:
        cs_preferred = float(cs_preferred)
    vol_reserve = float(data.get("volume_reserve_pct", 30))
    roughness = float(data.get("surface_roughness_ra", 0.8))

    # Validate material
    if mat_key not in MATERIALS_DB:
        return {"error": f"Material '{mat_key}' not found. Available: {list(MATERIALS_DB.keys())}"}
    mat = MATERIALS_DB[mat_key]
    E = _estimate_E(mat["hardness"])

    # Search O-ring candidates
    candidates = _search_oring(bore_or_rod, is_bore, cs_preferred)
    if not candidates:
        return {"error": f"No AS568 O-ring found for diameter {bore_or_rod}mm"}

    # Design for top candidates (max 5)
    results = []
    for oring in candidates[:5]:
        try:
            r = _design_single(oring, mat, E, application, pressure_dir,
                              sys_pressure, op_temp, vol_reserve, roughness,
                              bore_or_rod, is_bore)
            results.append(r)
        except Exception as e:
            pass

    # Sort by safety_margin descending
    results.sort(key=lambda x: x["safety_margin"], reverse=True)
    return _clean({"designs": results, "total_candidates": len(candidates)})


def _design_single(oring, mat, E, application, pressure_dir,
                   sys_pressure, op_temp, vol_reserve, roughness,
                   bore_or_rod, is_bore):
    """Design for a single O-ring candidate"""
    cs = oring["cs_mm"]
    id_mm = oring["id_mm"]
    od_mm = oring["od_mm"]

    warnings_list = []
    recommendations = []

    # 1. Target squeeze ratio
    sq_min, sq_target, sq_max = _get_squeeze_target(cs, application)

    # 2. Groove depth
    groove_depth = round(cs - (cs * sq_target / 100.0), 3)

